
public class DoWhileLoop {

	public static void main(String[] args) {
		// Declaring and initialization expression
        int i = 1;
 
        // Do-while loop
        do {
             // Print statement
            System.out.println("Good Day");
 
            // Update expression
            i++;
        }
 
        // Test expression
        while (i < 6);
	}

}
